	ALERT_ID varchar(40) NOT NULL,
    SERVER_ID varchar(50) NOT NULL,
	DESCRIPTION_ALERT nvarchar(512) NOT NULL,
	CREATED_AT DATETIME NOT NULL,
	SERVER_TYPE varchar(50)
) ENGINE = InnoDB;

